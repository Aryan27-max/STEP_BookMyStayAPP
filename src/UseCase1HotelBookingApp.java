/**
 * UseCase1HotelBookingApp
 *
 * This class represents the entry point of the Book My Stay
 * Hotel Booking Management System. It demonstrates how a Java
 * application starts execution and prints a welcome message
 * along with application details.
 *
 * @author Student
 * @version 1.0
 */
public class UseCase1HotelBookingApp {

    /**
     * Main method - Entry point of the Java application.
     * The JVM invokes this method when the program starts.
     *
     * @param args Command-line arguments (not used here)
     */
    public static void main(String[] args) {

        // Printing welcome message to console
        System.out.println("=================================");
        System.out.println("      Welcome to Book My Stay     ");
        System.out.println("   Hotel Booking Management App   ");
        System.out.println("          Version: 1.0            ");
        System.out.println("=================================");

        // Informing user about successful start
        System.out.println("Application started successfully.");
        System.out.println("Thank you for using Book My Stay!");
    }
}
